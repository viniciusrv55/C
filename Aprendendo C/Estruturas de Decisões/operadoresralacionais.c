#include <stdio.h>

int main(){

    int valorA,valorB;

    printf("Digite um valor: ");
    scanf("%d",&valorA);
    printf("Digite outro valor: ");
    scanf("%d",&valorB);

    printf("valorA > valorB resulta em %d \n", valorA>valorB);
    printf("valorA >= valorB resulta em %d \n", valorA>=valorB);
    printf("valorA < valorB resulta em %d \n", valorA<valorB);
    printf("valorA <= valorB resulta em %d \n", valorA<=valorB);
    printf("valorA == valorB resulta em %d \n", valorA==valorB);
    printf("valorA != valorB resulta em %d",valorA!=valorB);


return 0;}
